open class BasePresenter<V> {
    private var mBaseView: V? = null
    //绑定View
    fun bindView(mBaseView: V) {
        this.mBaseView = mBaseView
    }

    //解绑View
    fun unBindView() {
        mBaseView = null
    }

    //获取V层的方法
    fun getBaseView() = mBaseView
}